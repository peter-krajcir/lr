     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Task $task)
    {
        // dd(request()->all());

        if (request('completed')) {
            $task->completed = true;
        } else {
            $task->completed = false;
        }

        $task->update();

        return redirect('/projects/'.$task->project_id);
    }

